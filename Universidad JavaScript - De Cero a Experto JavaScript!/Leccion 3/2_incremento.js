let a = 4, b = 2;
let z = a + b;

//Incremento

//Pre-incremento

z = ++b //el valor se aumenta en 1 y ++ es antes de la variable

console.log(b);
console.log(z);

//Post-incremento

z = a++; // el valor aumenta en 1 y el ++ es despues de la variable
console.log(a);
console.log(z);



//Decremento

//Predecremento
z = --a
console.log(a);
console.log(z);

//Postdecremento
z = a--
console.log(a);
console.log(z);
